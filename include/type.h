#ifndef OS_TYPES_H_
#define OS_TYPES_H_

/* Signed.  */
typedef signed char	int8_t;
typedef short int	int16_t;
typedef int			int32_t;

/* Unsigned.  */
typedef unsigned char		uint8_t;
typedef unsigned short int	uint16_t;
typedef unsigned int		uint32_t;
typedef uint8_t 	u8;
typedef uint16_t 	u16;
typedef uint32_t 	u32;

typedef u32 size_t;

#endif /* OS_TYPES_H_ */
